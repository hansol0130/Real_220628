CREATE PROC [dbo].[USP_ZONE_LIST_JS]

/*
USP_ZONE_LIST_JS '179900'
*/

@CITY_CODE VARCHAR(30)

AS


SELECT ZONE_CODE, ZONE_NAME, CITY_CODE, MIN_LAT, MIN_LNG, MAX_LAT, MAX_LNG, COORD
FROM HTL_CODE_ZONE 
WHERE CITY_CODE=@CITY_CODE AND USE_YN='Y'
AND ISNULL(COORD, '') <> ''


GO
