CREATE PROC [dbo].[USP_ZONE_LIST]  
  
/*  
USP_ZONE_LIST '179900'  
*/  
  
@CITY_CODE VARCHAR(20)  
  
AS  
  
SELECT ZONE_CODE, ZONE_NAME, MIN_LAT, MIN_LNG, MAX_LAT, MAX_LNG  
FROM OMNIHOTEL.DBO.CHT_ZONE_CODE  
WHERE USE_YN='Y' AND CITY_CODE=(SELECT OLD_CODE FROM HTL_CODE_MAST_CITY WHERE CITY_CODE=@CITY_CODE) 



GO
