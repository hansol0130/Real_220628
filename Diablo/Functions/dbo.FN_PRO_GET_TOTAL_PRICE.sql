	DECLARE @TOT_PRICE DECIMAL

	SELECT @TOT_PRICE = ISNULL((SUM(A.SUM_SALE_PRICE) - SUM(A.SUM_DC_PRICE) + SUM(A.SUM_TAX_PRICE) + SUM(A.SUM_CHG_PRICE) + SUM(A.SUM_PENALTY_PRICE)), 0)
	FROM (
		SELECT B.PRO_CODE,
			ISNULL(SUM(CAST(A.SALE_PRICE AS DECIMAL)), 0.0) AS [SUM_SALE_PRICE],
			ISNULL(SUM(CAST(A.CHG_PRICE AS DECIMAL)), 0.0) AS [SUM_CHG_PRICE],
			ISNULL(SUM(CAST(A.DC_PRICE AS DECIMAL)), 0.0) AS [SUM_DC_PRICE],
			ISNULL(SUM(CAST(A.TAX_PRICE AS DECIMAL)), 0.0) AS [SUM_TAX_PRICE],
			ISNULL(SUM(CAST(A.PENALTY_PRICE AS DECIMAL)), 0.0) AS [SUM_PENALTY_PRICE]
		FROM RES_HTL_ROOM_MASTER A WITH(NOLOCK)
		INNER JOIN RES_MASTER_DAMO B WITH(NOLOCK) ON A.RES_CODE = B.RES_CODE
		WHERE B.PRO_CODE = @PRO_CODE AND A.RES_CODE LIKE 'RH%' AND B.RES_STATE <= 7
		GROUP BY B.PRO_CODE
		UNION ALL
		SELECT B.PRO_CODE,
			ISNULL(SUM(CAST(A.SALE_PRICE AS DECIMAL)), 0.0) AS [SUM_SALE_PRICE],
			ISNULL(SUM(CAST(A.CHG_PRICE AS DECIMAL)), 0.0) AS [SUM_CHG_PRICE],
			ISNULL(SUM(CAST(A.DC_PRICE AS DECIMAL)), 0.0) AS [SUM_DC_PRICE],
			ISNULL(SUM(CAST(A.TAX_PRICE AS DECIMAL)), 0.0) AS [SUM_TAX_PRICE],
			ISNULL(SUM(CAST(A.PENALTY_PRICE AS DECIMAL)), 0.0) AS [SUM_PENALTY_PRICE]
		FROM RES_CUSTOMER_DAMO A WITH(NOLOCK)
		INNER JOIN RES_MASTER_damo B WITH(NOLOCK) ON A.RES_CODE = B.RES_CODE
		WHERE B.PRO_CODE = @PRO_CODE AND A.RES_CODE LIKE 'R[P,T]%' AND (B.PRO_CODE LIKE 'KTR0%' OR A.RES_STATE IN (0, 3, 4))
		GROUP BY B.PRO_CODE
