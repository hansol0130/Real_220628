CREATE FUNCTION [dbo].[FN_RES_GET_CXL_COUNT]
(
	@RES_CODE VARCHAR(20)
)
RETURNS INT
AS
BEGIN

	DECLARE @COUNT INT

	SELECT @COUNT = COUNT(*) FROM RES_CUSTOMER_DAMO WITH(NOLOCK) 
	WHERE RES_CODE = @RES_CODE AND RES_STATE NOT IN (0, 4)

	RETURN (@COUNT)
END

GO
