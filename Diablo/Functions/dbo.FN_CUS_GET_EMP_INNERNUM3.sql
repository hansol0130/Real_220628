CREATE FUNCTION [dbo].[FN_CUS_GET_EMP_INNERNUM3]
(
	@EMP_CODE CHAR(7)
)
RETURNS VARCHAR(30)
AS
BEGIN

	DECLARE @EMP_INNERNUM3 VARCHAR(4)

	SELECT @EMP_INNERNUM3 = INNER_NUMBER3 FROM EMP_MASTER_damo WITH(NOLOCK) WHERE EMP_CODE = @EMP_CODE

	RETURN (@EMP_INNERNUM3)
END
GO
