CREATE FUNCTION [dbo].[FN_CUS_GET_EMP_FAX]
(
	@EMP_CODE CHAR(7)
)
RETURNS VARCHAR(50)
AS
BEGIN

	DECLARE @EMP_FAX VARCHAR(50)

	SELECT @EMP_FAX = PUB_VALUE
	FROM COD_PUBLIC WITH(NOLOCK)
	WHERE PUB_CODE = (SELECT TEAM_CODE FROM EMP_MASTER_damo WITH(NOLOCK) WHERE EMP_CODE = @EMP_CODE)
	AND PUB_TYPE = 'FAX.TEAM'

	RETURN (@EMP_FAX)
END
GO
