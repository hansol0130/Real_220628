CREATE PROCEDURE [dbo].[SP_FAX_CATEGORY_INSERT]
	@FAX_GROUP VARCHAR(3),
	@SUBJECT VARCHAR(50)
AS
BEGIN
 	SET NOCOUNT ON;
	DECLARE @FAX_CAT_SEQ INT;
	
	BEGIN
		
		SELECT @FAX_CAT_SEQ = (ISNULL(MAX(FAX_CAT_SEQ),0) + 1) FROM FAX_CATEGORY WHERE FAX_GROUP = @FAX_GROUP
	
		INSERT INTO FAX_CATEGORY
			(FAX_CAT_SEQ,FAX_GROUP,SUBJECT)
		VALUES
			(@FAX_CAT_SEQ,@FAX_GROUP,@SUBJECT)
	END
END






GO
