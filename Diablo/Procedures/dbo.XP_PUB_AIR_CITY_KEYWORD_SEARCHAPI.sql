	exec DBO.XP_PUB_AIR_CITY_KEYWORD_SEARCHAPI '세부' 
	exec DBO.XP_PUB_AIR_CITY_KEYWORD_SEARCHAPI '상해' 
	exec DBO.XP_PUB_AIR_CITY_KEYWORD_SEARCHAPI '홍차오' 
	exec DBO.XP_PUB_AIR_CITY_KEYWORD_SEARCHAPI '방콕' 

