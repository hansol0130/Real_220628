                      C.KOR_NAME AS REGION_NAME
FROM         dbo.PUB_CITY AS A WITH (NOLOCK) INNER JOIN
                      dbo.PUB_NATION AS B WITH (NOLOCK) ON B.NATION_CODE = A.NATION_CODE INNER JOIN
                      dbo.PUB_REGION AS C WITH (NOLOCK) ON C.REGION_CODE = B.REGION_CODE LEFT OUTER JOIN
                      dbo.PUB_STATE AS D WITH (NOLOCK) ON D.STATE_CODE = A.STATE_CODE AND B.NATION_CODE = D.NATION_CODE
