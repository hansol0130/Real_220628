-- SMS 리포트 오류 분석 리스트
CREATE VIEW [dbo].[V_SMS_ERROR_RESULT] (
    TRAN_DATE, TRAN_RSLT, SERVICE_TYPE, SERVICE_NO, RESULT_SEQ, SEQ
) AS
SELECT
    CONVERT(CHAR(8),TRAN_DATE,112) TRAN_DATE, TRAN_RSLT, SERVICE_TYPE, SERVICE_NO, RESULT_SEQ, SEQ
FROM EM_LOG
WHERE TRAN_RSLT NOT IN ('0')
GO
